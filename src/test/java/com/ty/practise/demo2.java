package com.ty.practise;

class demo2
{
	public static void main(String[] args)
	{
		int count=1;
		for(int i=1;i<=4;i++)
		{
			
			for(int j=4;j>i;j--)
			{
				System.out.print("hi");
			}
			for(int k=0;k<i;k++)
			{
				System.out.print(count);
				count--;
			}
			
			System.out.println();
		}
		
	}
	
}
	

	
	



